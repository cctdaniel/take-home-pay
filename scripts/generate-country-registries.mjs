import { existsSync, mkdirSync, readdirSync, statSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(scriptDir, "..");

function posixPath(value) {
  return value.split(path.sep).join(path.posix.sep);
}

function ensureParent(filePath) {
  mkdirSync(path.dirname(filePath), { recursive: true });
}

function writeGenerated(filePath, content) {
  ensureParent(filePath);
  writeFileSync(
    filePath,
    `${content.trimEnd()}\n`,
  );
}

function getDirectories(relativePath) {
  const absolutePath = path.join(rootDir, relativePath);

  if (!existsSync(absolutePath)) {
    return [];
  }

  return readdirSync(absolutePath)
    .filter((entry) => statSync(path.join(absolutePath, entry)).isDirectory())
    .sort((a, b) => a.localeCompare(b));
}

function getFiles(relativePath, predicate) {
  const absolutePath = path.join(rootDir, relativePath);

  if (!existsSync(absolutePath)) {
    return [];
  }

  return readdirSync(absolutePath)
    .filter((entry) => statSync(path.join(absolutePath, entry)).isFile())
    .filter(predicate)
    .sort((a, b) => a.localeCompare(b));
}

function toCountryCode(value) {
  return value.toUpperCase();
}

function toIdentifierPrefix(countryCode) {
  return `${countryCode.replace(/[^A-Z0-9]/g, "")}`;
}

function getCountryDirectories() {
  return getDirectories("lib/countries").filter((directory) => {
    const countryPath = path.join(rootDir, "lib/countries", directory);
    return (
      existsSync(path.join(countryPath, "index.ts")) &&
      existsSync(path.join(countryPath, "calculator.ts"))
    );
  });
}

function generateCalculatorRegistry(countryDirectories) {
  const imports = countryDirectories
    .map((directory) => {
      const countryCode = toCountryCode(directory);
      return `import { ${countryCode}Calculator } from "./${directory}";`;
    })
    .join("\n");
  const entries = countryDirectories
    .map((directory) => {
      const countryCode = toCountryCode(directory);
      return `  { code: ${countryCode}Calculator.countryCode, calculator: ${countryCode}Calculator },`;
    })
    .join("\n");

  writeGenerated(
    path.join(rootDir, "lib/countries/registry.generated.ts"),
    `// This file is auto-generated by scripts/generate-country-registries.mjs.
// Do not edit by hand.

${imports}
import type { CountryCalculator, CountryCode } from "./types";

export interface CountryRegistryEntry {
  code: CountryCode;
  calculator: CountryCalculator;
}

export const COUNTRY_REGISTRY: CountryRegistryEntry[] = [
${entries}
].sort((a, b) => a.calculator.config.name.localeCompare(b.calculator.config.name));`,
  );
}

function generateResultBreakdowns() {
  const files = getFiles(
    "components/calculator/results",
    (file) =>
      file.endsWith("-result-breakdown.tsx") &&
      file !== "country-result-breakdown.tsx",
  );

  const imports = files
    .map((file) => {
      const countryCode = toCountryCode(file.replace("-result-breakdown.tsx", ""));
      return `import { ${countryCode}ResultBreakdown } from "./${file.replace(".tsx", "")}";`;
    })
    .join("\n");
  const entries = files
    .map((file) => {
      const countryCode = toCountryCode(file.replace("-result-breakdown.tsx", ""));
      return `  ${countryCode}: ${countryCode}ResultBreakdown,`;
    })
    .join("\n");

  writeGenerated(
    path.join(rootDir, "components/calculator/results/country-result-breakdowns.generated.tsx"),
    `// This file is auto-generated by scripts/generate-country-registries.mjs.
// Do not edit by hand.

import type { CountryCode } from "@/lib/countries/types";
import type { ReactNode } from "react";
${imports ? `${imports}\n` : ""}import type { CountryResultBreakdownProps } from "./types";

export type CountryResultBreakdownComponent = (
  props: CountryResultBreakdownProps,
) => ReactNode;

export const COUNTRY_RESULT_BREAKDOWNS: Partial<
  Record<CountryCode, CountryResultBreakdownComponent>
> = {
${entries}
};`,
  );
}

function generateCountryExtensions() {
  const files = getFiles(
    "components/calculator/country-extensions",
    (file) => file.endsWith(".tsx") && !file.endsWith(".test.tsx"),
  );

  const imports = files
    .map((file) => {
      const countryCode = toCountryCode(file.replace(".tsx", ""));
      const identifier = `${toIdentifierPrefix(countryCode)}CountryExtension`;
      return `import ${identifier} from "./country-extensions/${file.replace(".tsx", "")}";`;
    })
    .join("\n");
  const entries = files
    .map((file) => {
      const countryCode = toCountryCode(file.replace(".tsx", ""));
      const identifier = `${toIdentifierPrefix(countryCode)}CountryExtension`;
      return `  ${countryCode}: ${identifier},`;
    })
    .join("\n");

  writeGenerated(
    path.join(rootDir, "components/calculator/country-extensions.generated.tsx"),
    `// This file is auto-generated by scripts/generate-country-registries.mjs.
// Do not edit by hand.

import type { CountryCode } from "@/lib/countries/types";
${imports ? `${imports}\n` : ""}import type { CountryCalculatorExtensionComponent } from "./country-extension";

export const COUNTRY_CALCULATOR_EXTENSIONS: Partial<
  Record<CountryCode, CountryCalculatorExtensionComponent>
> = {
${entries}
};`,
  );
}

function generateCompareAdapters(countryDirectories) {
  const compareModules = countryDirectories.filter((directory) =>
    existsSync(path.join(rootDir, "lib/countries", directory, "compare.ts")),
  );
  const imports = compareModules
    .map((directory) => {
      const countryCode = toCountryCode(directory);
      const identifier = `${toIdentifierPrefix(countryCode)}CountryComparisonAdapter`;
      const importPath = posixPath(`@/lib/countries/${directory}/compare`);
      return `import { buildCountryComparison as ${identifier} } from "${importPath}";`;
    })
    .join("\n");
  const entries = compareModules
    .map((directory) => {
      const countryCode = toCountryCode(directory);
      const identifier = `${toIdentifierPrefix(countryCode)}CountryComparisonAdapter`;
      return `  ${countryCode}: ${identifier},`;
    })
    .join("\n");

  writeGenerated(
    path.join(rootDir, "hooks/country-comparison-adapters.generated.ts"),
    `// This file is auto-generated by scripts/generate-country-registries.mjs.
// Do not edit by hand.

import type { CountryCode } from "@/lib/countries/types";
${imports ? `${imports}\n` : ""}import type { CountryComparisonAdapter } from "./use-country-comparison";

export const COUNTRY_COMPARISON_ADAPTERS: Partial<
  Record<CountryCode, CountryComparisonAdapter>
> = {
${entries}
};`,
  );
}

const countryDirectories = getCountryDirectories();

generateCalculatorRegistry(countryDirectories);
generateResultBreakdowns();
generateCountryExtensions();
generateCompareAdapters(countryDirectories);
